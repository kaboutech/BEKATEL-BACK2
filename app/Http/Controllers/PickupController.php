        $formattedbons = $bons->map(function ($bon) {
            return [
                'Ref' => $bon->ref,
                'Ville' => $bon->cities_id,
                'Montant' => $bon->amount,
            ];
        });


        return response()->json([ "data"=>$formattedbons]);
    }

